    private void BotaoCarregarImagemMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BotaoCarregarImagemMousePressed
        BotaoCarregarImagem.setBackground(Color.decode("#bcbcbc"));

        String endereco = "src\\Imagens\\";
        JFileChooser jfc = new JFileChooser(endereco);
        FileNameExtensionFilter filter = new FileNameExtensionFilter("JPG images", "jpg");
        jfc.setFileFilter(filter);
        
        
        if (jfc.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            endereco += jfc.getName(jfc.getSelectedFile());
            
            this.imagemOriginal = Imgcodecs.imread(endereco);

            jTextField1.setVisible(false);
            BotaoAplicar.setVisible(false);
            LabelImagemOriginal.setVisible(true);
            LabelImagemOriginal.setText("");
            LabelImagemAlterada.setVisible(false);
            LabelImagemAlterada2.setVisible(false);
            LabelImagemAlterada3.setVisible(false);
            
            
            
            iconOriginal = new ImageIcon(Metodos.toBufferedImage(imagemOriginal));
            
            //LabelImagemOriginal.setIcon(iconOriginal);
            
            image = iconOriginal.getImage();  
            newimg = image.getScaledInstance(300, 168, java.awt.Image.SCALE_SMOOTH);  
            LabelImagemOriginal.setIcon(new ImageIcon(newimg));
            
            
        }else{
            imagemCarregada = "";
            CaminhoImagemCarregada = "";
            imagemOriginal = null;
        
        }
    }//GEN-LAST:event_BotaoCarregarImagemMousePressed

