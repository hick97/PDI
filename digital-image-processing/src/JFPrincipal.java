    private void BotaoAplicarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BotaoAplicarMousePressed
        BotaoAplicar.setBackground(pressed);
        Double valor;
        int tam;
           
        switch(opcao){
            case 4:
                LabelImagemAlterada.setVisible(true);
                LabelImagemAlterada.setText("Brilho aplicado nas bandas RGB:");
                LabelImagemAlterada2.setVisible(true);
                LabelImagemAlterada2.setText("Brilho aplicado na banda Y:");
        
                valor = Double.parseDouble(jTextField1.getText());
                jTextField1.setText("Digite o valor do brilho aditivo");
                
                //Brilho Soma RGB
                imagemAlterada = Metodos.brilhoSomaRGB(imagemOriginal, valor);
                iconAlterado = new ImageIcon(Metodos.toBufferedImage(imagemAlterada));
                LabelImagemAlterada.setIcon(iconAlterado);
                
                image = iconAlterado.getImage();  
                newimg = image.getScaledInstance(300, 168, java.awt.Image.SCALE_SMOOTH);  
                LabelImagemAlterada.setIcon(new ImageIcon(newimg));
                
                //#################################################################################
                
                //Brilho Soma RGB-YIQ-RGB
                //Primeiro converte pra YIQ
                imagemAlterada = Metodos.converterRGBYIQ(imagemOriginal);
                //Aplica o brilho ainda em YIQ                                   
                imagemAlterada = Metodos.brilhoSomaYIQ(imagemAlterada, valor);
                //Depois de aplicar o brilho, volta para RGB
                imagemAlterada = Metodos.converterYIQRGB(imagemAlterada);
                
                iconAlterado = new ImageIcon(Metodos.toBufferedImage(imagemAlterada));
                LabelImagemAlterada2.setIcon(iconAlterado);
                
                image = iconAlterado.getImage();  
                newimg = image.getScaledInstance(300, 168, java.awt.Image.SCALE_SMOOTH);  
                LabelImagemAlterada2.setIcon(new ImageIcon(newimg));
                
                
                break;
                
            case 5:
                LabelImagemAlterada.setVisible(true);
                LabelImagemAlterada.setText("Brilho aplicado nas bandas RGB");
                LabelImagemAlterada2.setVisible(true);
                LabelImagemAlterada2.setText("Brilho aplicado na banda Y");
                
                valor = Double.parseDouble(jTextField1.getText());
                jTextField1.setText("Digite o valor do brilho multiplicativo");
                
                //Brilh multiplicação RGB
                imagemAlterada = Metodos.brilhoMultiplicacaoRGB(imagemOriginal, valor);
                iconAlterado = new ImageIcon(Metodos.toBufferedImage(imagemAlterada));
                //LabelImagemAlterada.setIcon(iconAlterado);
                
                image = iconAlterado.getImage();  
                newimg = image.getScaledInstance(300, 168, java.awt.Image.SCALE_SMOOTH);  
                LabelImagemAlterada.setIcon(new ImageIcon(newimg));
                
                //#################################################################################
                
                //Brilho multiplicação RGB-YIQ-RGB
                //Primeiro converte pra YIQ
                imagemAlterada = Metodos.converterRGBYIQ(imagemOriginal);
                //Aplica o brilho ainda em YIQ                                   
                imagemAlterada = Metodos.brilhoMultiplicacaoYIQ(imagemAlterada, valor);
                //Depois de aplicar o brilho, volta para RGB
                imagemAlterada = Metodos.converterYIQRGB(imagemAlterada);
                
                iconAlterado = new ImageIcon(Metodos.toBufferedImage(imagemAlterada));
                LabelImagemAlterada2.setIcon(iconAlterado);  
                
                image = iconAlterado.getImage();  
                newimg = image.getScaledInstance(300, 168, java.awt.Image.SCALE_SMOOTH);  
                LabelImagemAlterada2.setIcon(new ImageIcon(newimg));
                
                break;
                
            case 6:
                LabelImagemAlterada.setVisible(true);
                LabelImagemAlterada.setText("");
                
                try{
                    tam = Integer.parseInt(jTextField1.getText());
                }catch (NumberFormatException e){
                    tam = -1;
                }
                jTextField1.setText("Digite o valor do limiar");
                imagemAlterada = Metodos.limiarizaçãoY(imagemOriginal, tam);
                iconAlterado = new ImageIcon(Metodos.toBufferedImage(imagemAlterada));
                LabelImagemAlterada.setIcon(iconAlterado);               
                break;
            case 7:
                LabelImagemAlterada.setVisible(true);
                LabelImagemAlterada.setText("");
                
                tam = Integer.parseInt(jTextField1.getText());
                jTextField1.setText("Digite o valor do kernel");
                imagemAlterada = Metodos.filtroMedia(imagemOriginal, tam);
                iconAlterado = new ImageIcon(Metodos.toBufferedImage(imagemAlterada));
                LabelImagemAlterada.setIcon(iconAlterado);
                break;
                
            case 8:
                LabelImagemAlterada.setVisible(true);
                LabelImagemAlterada.setText("");
                
                tam = Integer.parseInt(jTextField1.getText());
                jTextField1.setText("Digite o valor do kernel");
                imagemAlterada = Metodos.filtroMediana(imagemOriginal, tam);           
                iconAlterado = new ImageIcon(Metodos.toBufferedImage(imagemAlterada));
                LabelImagemAlterada.setIcon(iconAlterado);
                break;
        }
    }//GEN-LAST:event_BotaoAplicarMousePressed

